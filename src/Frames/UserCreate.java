    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(!jTextField4.getText().isEmpty()){
            int value= Integer.parseInt(jTextField4.getText());
            User us= has.searchUser(value);
            if(us!=null){
                int dat;
                boolean exit = false;
                dat = Integer.parseInt(JOptionPane.showInputDialog("1) Deseas modificar el nombre: \n" + "2) Desea modificar el tipo de licencia : \n" + "3)Salir"));
                if(dat==1){                
                    String name = JOptionPane.showInputDialog("Digite el nuevo nombre"); 
                    has.editName(us.getId(), name);
                    return;
                }
                
                if(dat==2){
                    String license = JOptionPane.showInputDialog("Digite la nueva licencia"); 
                    
                    if(license.equals("B1") || license.equals("B2") || license.equals("B3") || license.equals("B4")){
                        has.editarLicense(us.getId(), license);  
                        return;
                    }
                    JOptionPane.showMessageDialog(null, "La opción no es valida ");
                    return;
                }
                if(dat==3){
                    exit = true;
                    

                }else{
                    JOptionPane.showMessageDialog(null, "La opción no es valida ");
                }
            }
            
            
            
            

        }else{
            JOptionPane.showMessageDialog(null, "Debe de ingresar un Usuario ");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

