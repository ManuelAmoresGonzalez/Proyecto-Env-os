    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id= Integer.parseInt(jTextField3.getText());
        String name= jTextField2.getText();
        String valueCom = (String) jComboBox1.getSelectedItem();
        if(valueCom.equals("Licencia")){
            JOptionPane.showMessageDialog(null, "Debe de elegir un tipo de licencia");
            jComboBox1.setSelectedIndex(0);
        }else{
            User newUser= new User(id, name, valueCom);
            boolean value= has.put(newUser);
            if(value==true){
                JOptionPane.showMessageDialog(null, "Insertado correctamente");
            }else{
                JOptionPane.showMessageDialog(null, "Repetido");
            }
            
        }              
    }//GEN-LAST:event_jButton1ActionPerformed

