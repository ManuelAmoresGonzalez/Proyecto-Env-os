    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Orders aux= ord.root;
        if(!jTextField3.getText().isEmpty()){
            int id= Integer.parseInt(jTextField3.getText());
            Orders sear= ord.searchOrder(aux, id);        
            if(sear!=null){
                boolean val= ord.deleteOrder(id);
                if(val==true){
                    JOptionPane.showMessageDialog(null, "Eliminado correctamente");
                }else{
                    JOptionPane.showMessageDialog(null, "No se encuentra");
                }
            }else{
                JOptionPane.showMessageDialog(null, "No se encuentra");
            } 
        }else{
            JOptionPane.showMessageDialog(null, "Debe de insertar un numero de pedido");
        }
           
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        
        try {
            int id= Integer.parseInt(jTextField3.getText());
            int value= ord.editOrder(id);
            switch (value) {
                case 1:
                    JOptionPane.showMessageDialog(null, "Editado correctamente");
                    break;
                case 2:
                    JOptionPane.showMessageDialog(null, "El nuevo id debe de ser un numero");
                    break;    
                case 3:
                    JOptionPane.showMessageDialog(null, "El id no se encuentra");
                    break;
                case 4:
                    JOptionPane.showMessageDialog(null, "El nuevo origen no exite");
                    break;
                case 5:
                    JOptionPane.showMessageDialog(null, "El nuevo destino no existe");
                    break;
                case 6:
                    JOptionPane.showMessageDialog(null, "El destino y el origen no pueden ser iguales");
                    break;
                case 7:
                    JOptionPane.showMessageDialog(null, "El peso deben de ser numeros ");
                    break;
                case 8:
                    JOptionPane.showMessageDialog(null, "Opción invalida ");
                    break;
                default:
                    break;
            }
        }
        catch(HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "El id no se encuentra");
            jTextField3.setText(null);            
        }             
    }//GEN-LAST:event_jButton3ActionPerformed
    
