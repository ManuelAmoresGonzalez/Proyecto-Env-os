 public void chooseLicense(){
    if(!jTextField4.getText().isEmpty()){
        int Weig= Integer.parseInt(jTextField4.getText());         
        jComboBox1.removeAllItems();       
        if(Weig<=1500){
            for (User table : this.has.table){
                for (User aux = table; aux != null; aux = aux.Next()){
                    if(aux.getLicense().equals("B1")){
                        jComboBox1.addItem(Integer.toString(aux.getId()));                        
                    }                
                }            
            }            
        }    
        
        if(Weig>1500 & Weig<5000){
            for (User table : this.has.table){
                for (User aux = table; aux != null; aux = aux.Next()){
                    if(aux.getLicense().equals("B2")){
                        jComboBox1.addItem(Integer.toString(aux.getId()));
                        
                    }                
                }            
            }                          
        }
        
        if(Weig>=5000 & Weig<15000){
            for (User table : this.has.table){
                for (User aux = table; aux != null; aux = aux.Next()){
                    if(aux.getLicense().equals("B3")){
                        jComboBox1.addItem(Integer.toString(aux.getId()));
                    }                
                }            
            }                
        }
        
        if(Weig>=15000){
            for (User table : this.has.table){
                for (User aux = table; aux != null; aux = aux.Next()){
                    if(aux.getLicense().equals("B4")){
                        jComboBox1.addItem(Integer.toString(aux.getId()));
                        
                    }                
                }            
            }                     
        }
    }else{
        JOptionPane.showMessageDialog(null, "Debe de insertar peso para ver los choferes disponibles");
    } 
                
    } 
 
 
